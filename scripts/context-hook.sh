#!/bin/bash
set -uo pipefail

# context-hook.sh — Inject autoresearch context on UserPromptSubmit.
#
# When autoresearch.jsonl exists in the current working directory:
# 1. Enforces loop continuation (DO NOT STOP)
# 2. Injects domain-specific exploration hints based on optimization category
#
# Category detection: scans config name + metricName for keywords.

HOOK_INPUT=$(cat)

CWD=$(echo "$HOOK_INPUT" | jq -r '.cwd // empty')
if [[ -z "$CWD" ]]; then
  exit 0
fi

JSONL="$CWD/autoresearch.jsonl"
if [[ ! -f "$JSONL" ]]; then
  exit 0
fi

# Don't inject on stop commands
PROMPT=$(echo "$HOOK_INPUT" | jq -r '.prompt // empty')
if [[ "$PROMPT" =~ autoresearch:stop ]] || [[ "$PROMPT" =~ autoresearch-stop ]]; then
  exit 0
fi

# --- Session stats ---
TOTAL=$(grep -c '"status"' "$JSONL" 2>/dev/null) || TOTAL=0
KEPT=$(grep -c '"keep"' "$JSONL" 2>/dev/null) || KEPT=0
DISCARDED=$(grep -c '"discard"' "$JSONL" 2>/dev/null) || DISCARDED=0

# --- Hard cap on total experiments ---
CONFIG_LINE_CAP=$(grep '"type":"config"' "$JSONL" 2>/dev/null | head -1)
MAX_EXPERIMENTS=$(echo "$CONFIG_LINE_CAP" | jq -r '.maxExperiments // empty' 2>/dev/null)
if [[ -n "$MAX_EXPERIMENTS" ]] && [[ "$MAX_EXPERIMENTS" =~ ^[0-9]+$ ]] && [[ $TOTAL -ge $MAX_EXPERIMENTS ]]; then
  jq -nc --arg ctx "AUTORESEARCH COMPLETE. Reached max experiments ($MAX_EXPERIMENTS). $TOTAL runs, $KEPT kept. STOP NOW — run /claudecode-autoresearch:autoresearch-status for the final dashboard." \
    '{additionalContext: $ctx}'
  exit 0
fi

# --- Detect plateau (last 5 discarded) ---
PLATEAU=""
if [[ $DISCARDED -ge 5 ]]; then
  LAST5=$(grep '"status"' "$JSONL" | tail -5 | jq -r '.status' 2>/dev/null)
  DISCARD_COUNT=$(echo "$LAST5" | grep -c 'discard' || true)
  if [[ "$DISCARD_COUNT" -ge 4 ]]; then
    PLATEAU=" WARNING: You are plateauing (${DISCARD_COUNT}/5 recent discards). Step back. Re-read the source files. Try a structurally different approach — not incremental tweaks."
  fi
fi

# --- Read config for category detection ---
CONFIG_LINE=$(grep '"type":"config"' "$JSONL" 2>/dev/null | head -1)
CONFIG_NAME=$(echo "$CONFIG_LINE" | jq -r '.name // empty' 2>/dev/null)
METRIC_NAME=$(echo "$CONFIG_LINE" | jq -r '.metricName // empty' 2>/dev/null)
SEARCH_TEXT=$(echo "$CONFIG_NAME $METRIC_NAME" | tr '[:upper:]' '[:lower:]')

# --- Domain-specific exploration hints ---
DOMAIN_HINT=""

# Performance / Speed
if echo "$SEARCH_TEXT" | grep -qE 'perf|speed|fast|latency|throughput|_us|_ms|_ns|runtime|bench|render|fps'; then
  DOMAIN_HINT="EXPLORATION STRATEGY (performance): Profile before guessing. Use node --prof / --cpu-prof, console.time, perf, flamegraphs. Check: hot loops, allocations, string concatenation, regex in hot paths, unnecessary copies, cache misses, function call overhead, megamorphic call sites. Try: algorithmic changes before micro-opts, precomputation, lookup tables, avoid allocations in hot paths, batch operations, flatten data structures. When stuck: read the generated bytecode (node --print-bytecode), check V8 optimization status (--trace-opt --trace-deopt)."

# Bundle size / Build
elif echo "$SEARCH_TEXT" | grep -qE 'bundle|size|build|webpack|vite|esbuild|rollup|minif|tree.?shak|bytes|kb|mb'; then
  DOMAIN_HINT="EXPLORATION STRATEGY (bundle/build): Analyze before cutting. Use source-map-explorer, webpack-bundle-analyzer, or esbuild --analyze. Check: duplicate dependencies, barrel file re-exports, large polyfills, unused exports, dynamic imports for heavy routes. Try: tree-shaking barriers, code splitting, lighter alternatives (date-fns vs moment, preact vs react), dead code elimination, CSS purging."

# Test speed
elif echo "$SEARCH_TEXT" | grep -qE 'test|jest|vitest|mocha|pytest|spec|suite'; then
  DOMAIN_HINT="EXPLORATION STRATEGY (test speed): Profile the test runner, not just the code. Check: setup/teardown time, fixture loading, sequential vs parallel, heavy imports, real I/O in unit tests. Try: in-memory databases, worker threads/sharding, lazy imports, mock heavy deps, snapshot instead of deep-equal, happy-dom vs jsdom. When stuck: run with --reporter=verbose to find the slowest individual tests."

# Training / ML
elif echo "$SEARCH_TEXT" | grep -qE 'loss|train|epoch|val_|accuracy|bpb|perplexity|gradient|lr|learning.?rate'; then
  DOMAIN_HINT="EXPLORATION STRATEGY (training): Monitor loss curves and gradient norms. Check: learning rate schedule, batch size effects, data loading bottlenecks, GPU utilization. Try: warmup schedules, weight decay, gradient clipping, mixed precision, architecture changes (depth vs width), data augmentation, curriculum learning. When stuck: plot the loss landscape, check for dead neurons, verify data pipeline correctness."

# UX / Design / Frontend
elif echo "$SEARCH_TEXT" | grep -qE 'ux|design|ui|lighthouse|cls|lcp|fid|inp|accessibility|a11y|score|user|experience|visual'; then
  DOMAIN_HINT="EXPLORATION STRATEGY (UX/frontend): Look at actual output, don't just read code. Take screenshots, check rendering, test at different viewports. Check: layout shifts (CLS), loading sequence, interaction delays, contrast ratios, keyboard navigation. Try: lazy loading, image optimization, font loading strategies, critical CSS inlining, skeleton screens, progressive enhancement. Use Lighthouse, axe-core, or PageSpeed Insights for structured feedback."

# Code quality / Lines / Complexity
elif echo "$SEARCH_TEXT" | grep -qE 'line|loc|complex|maintainab|readab|duplicat|debt|lint|quality'; then
  DOMAIN_HINT="EXPLORATION STRATEGY (code quality): Measure before refactoring. Check: cyclomatic complexity, duplicate blocks, dead code, deep nesting, long parameter lists. Try: extract functions, flatten conditionals (early return), replace conditionals with polymorphism, consolidate duplicate logic, remove dead exports. When stuck: look at the call graph — high fan-in functions are refactoring targets."

# Memory
elif echo "$SEARCH_TEXT" | grep -qE 'memory|heap|rss|gc|leak|alloc|retain'; then
  DOMAIN_HINT="EXPLORATION STRATEGY (memory): Measure with heap snapshots, not intuition. Use --inspect + Chrome DevTools, process.memoryUsage(), or valgrind. Check: retained references, closures capturing large scopes, growing arrays/maps, event listener leaks, circular references. Try: WeakRef/WeakMap, object pooling, streaming instead of buffering, reduce scope captures, explicit nulling in long-lived contexts."
fi

# --- Compose context ---
CHECKS=""
if [[ -x "$CWD/autoresearch.checks.sh" ]]; then
  CHECKS=" Backpressure checks active."
fi

IDEAS=""
if [[ -f "$CWD/autoresearch.ideas.md" ]]; then
  IDEAS=" Check autoresearch.ideas.md for queued ideas."
fi

ENFORCE="AUTORESEARCH MODE ACTIVE ($TOTAL runs, $KEPT kept).${CHECKS}${IDEAS}${PLATEAU}

MANDATORY RULES — VIOLATIONS WILL WASTE THE USER'S COMPUTE BUDGET:
- DO NOT STOP. DO NOT ASK \"should I continue?\". DO NOT PAUSE FOR CONFIRMATION.
- Run the next experiment immediately after logging the previous one.
- If you are stuck, try something structurally different. Re-read source files.
- If you plateau, look at the problem from a completely different angle.
- Read autoresearch.md for session context. Use the plugin scripts."

if [[ -n "$DOMAIN_HINT" ]]; then
  ENFORCE="$ENFORCE

$DOMAIN_HINT"
fi

jq -nc --arg ctx "$ENFORCE" '{additionalContext: $ctx}'
